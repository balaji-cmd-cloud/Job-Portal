document.addEventListener('DOMContentLoaded', () => {
    // 1. Dark/Light Mode Theme Toggle Logic
    const htmlElement = document.documentElement;
    const themeToggleBtn = document.getElementById('themeToggleBtn');
    const themeToggleIcon = document.getElementById('themeToggleIcon');

    // Retrieve saved theme or default to system preference
    const savedTheme = localStorage.getItem('theme');
    if (savedTheme) {
        htmlElement.setAttribute('data-theme', savedTheme);
        updateThemeIcon(savedTheme);
    } else {
        const systemPrefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
        const defaultTheme = systemPrefersDark ? 'dark' : 'light';
        htmlElement.setAttribute('data-theme', defaultTheme);
        updateThemeIcon(defaultTheme);
    }

    if (themeToggleBtn) {
        themeToggleBtn.addEventListener('click', () => {
            const currentTheme = htmlElement.getAttribute('data-theme');
            const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
            htmlElement.setAttribute('data-theme', newTheme);
            localStorage.setItem('theme', newTheme);
            updateThemeIcon(newTheme);
        });
    }

    function updateThemeIcon(theme) {
        if (!themeToggleIcon) return;
        if (theme === 'dark') {
            themeToggleIcon.className = 'bi bi-moon-stars-fill text-warning';
        } else {
            themeToggleIcon.className = 'bi bi-sun-fill text-secondary';
        }
    }

    // 2. Chatbot Panel Toggle and API Connection
    const chatToggle = document.getElementById('chatToggle');
    const chatContainer = document.getElementById('chatContainer');
    const closeChat = document.getElementById('closeChat');
    const chatForm = document.getElementById('chatForm');
    const chatInput = document.getElementById('chatInput');
    const chatBody = document.getElementById('chatBody');

    if (chatToggle && chatContainer) {
        chatToggle.addEventListener('click', () => {
            chatContainer.classList.toggle('minimized');
            chatToggle.style.display = chatContainer.classList.contains('minimized') ? 'flex' : 'none';
            // Scroll to bottom on open
            scrollToBottom();
        });
    }

    if (closeChat && chatContainer && chatToggle) {
        closeChat.addEventListener('click', () => {
            chatContainer.classList.add('minimized');
            chatToggle.style.display = 'flex';
        });
    }

    if (chatForm && chatInput && chatBody) {
        chatForm.addEventListener('submit', async (e) => {
            e.preventDefault();
            const message = chatInput.value.trim();
            if (!message) return;

            // Append User Message
            appendMessage(message, 'user');
            chatInput.value = '';

            // Show Typing Indicator
            const typingIndicator = showTypingIndicator();

            try {
                // Post to chatbot endpoint
                const response = await fetch('/chatbot/api/', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRFToken': getCookie('csrftoken')
                    },
                    body: JSON.stringify({ message: message })
                });

                // Remove Typing Indicator
                typingIndicator.remove();

                if (response.ok) {
                    const data = await response.json();
                    appendMessage(data.response, 'bot');
                } else {
                    appendMessage('Sorry, there was an issue processing your request. Please try again.', 'bot');
                }
            } catch (err) {
                typingIndicator.remove();
                appendMessage('Network error. Please check your connection.', 'bot');
            }
        });
    }

    function appendMessage(content, sender) {
        if (!chatBody) return;
        const msgDiv = document.createElement('div');
        msgDiv.className = `message-bubble ${sender}`;
        
        if (sender === 'bot') {
            // Safe HTML injection (content generated by trusted server backend logic)
            msgDiv.innerHTML = content;
        } else {
            msgDiv.textContent = content;
        }
        
        chatBody.appendChild(msgDiv);
        scrollToBottom();
    }

    function showTypingIndicator() {
        if (!chatBody) return null;
        const indicatorDiv = document.createElement('div');
        indicatorDiv.className = 'message-bubble bot';
        indicatorDiv.innerHTML = `
            <div class="typing-indicator">
                <span></span>
                <span></span>
                <span></span>
            </div>
        `;
        chatBody.appendChild(indicatorDiv);
        scrollToBottom();
        return indicatorDiv;
    }

    function scrollToBottom() {
        if (chatBody) {
            chatBody.scrollTop = chatBody.scrollHeight;
        }
    }

    // 3. Saved Jobs AJAX Actions
    const saveButtons = document.querySelectorAll('.save-job-btn');
    saveButtons.forEach(button => {
        button.addEventListener('click', async (e) => {
            e.preventDefault();
            const jobId = button.dataset.jobId;
            if (!jobId) return;

            try {
                const response = await fetch(`/jobs/api/${jobId}/save/`, {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRFToken': getCookie('csrftoken')
                    }
                });

                if (response.ok) {
                    const data = await response.json();
                    if (data.saved) {
                        button.innerHTML = '<i class="bi bi-bookmark-fill me-1"></i> Saved';
                        button.classList.remove('btn-outline-primary');
                        button.classList.add('btn-primary');
                        showToast('Job saved successfully!');
                    } else {
                        button.innerHTML = '<i class="bi bi-bookmark me-1"></i> Save';
                        button.classList.remove('btn-primary');
                        button.classList.add('btn-outline-primary');
                        showToast('Job removed from saved list.');
                    }
                } else if (response.status === 403) {
                    showToast('Please log in as a candidate to save jobs.');
                }
            } catch (err) {
                console.error(err);
            }
        });
    });

    // Helper: Retrieve CSRF Token from Cookies
    function getCookie(name) {
        let cookieValue = null;
        if (document.cookie && document.cookie !== '') {
            const cookies = document.cookie.split(';');
            for (let i = 0; i < cookies.length; i++) {
                const cookie = cookies[i].trim();
                if (cookie.substring(0, name.length + 1) === (name + '=')) {
                    cookieValue = decodeURIComponent(cookie.substring(name.length + 1));
                    break;
                }
            }
        }
        return cookieValue;
    }

    // Helper: Dynamic Toast Notifications
    function showToast(message) {
        // Build a bootstrap toast container dynamically if not present
        let toastContainer = document.getElementById('toast-container');
        if (!toastContainer) {
            toastContainer = document.createElement('div');
            toastContainer.id = 'toast-container';
            toastContainer.className = 'position-fixed bottom-0 start-0 p-3';
            toastContainer.style.zIndex = '1100';
            document.body.appendChild(toastContainer);
        }

        const toastId = 'toast-' + Date.now();
        const toastHtml = `
            <div id="${toastId}" class="toast align-items-center text-white bg-dark border-0 custom-toast" role="alert" aria-live="assertive" aria-atomic="true">
                <div class="d-flex">
                    <div class="toast-body">
                        ${message}
                    </div>
                    <button type="button" class="btn-close btn-close-white me-2 m-auto" data-bs-dismiss="toast" aria-label="Close"></button>
                </div>
            </div>
        `;
        toastContainer.insertAdjacentHTML('beforeend', toastHtml);

        const toastElement = document.getElementById(toastId);
        const toast = new bootstrap.Toast(toastElement, { delay: 3000 });
        toast.show();

        toastElement.addEventListener('hidden.bs.toast', () => {
            toastElement.remove();
        });
    }
});
